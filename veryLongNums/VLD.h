#pragma once
#include "generalOperations.h"

class VLD;

class VLD : private
	GeneralOp
{
protected:
	struct Number
	{
		GeneralOp::Number number[MAX_CELLS];
		double fraction = 0;
	};

	friend VLD& operator + (const VLD&  left, const VLD& right);
	friend VLD& operator - (const VLD&  left, const VLD& right);
	friend VLD& operator * (const VLD&  left, const VLD& right);
	friend VLD& operator / (const VLD&  left, const VLD& right);

	friend bool operator >  (const VLD&  left, const VLD& right);
	friend bool operator >= (const VLD&  left, const VLD& right);
	friend bool operator <  (const VLD&  left, const VLD& right);
	friend bool operator <= (const VLD&  left, const VLD& right);
	friend bool operator == (const VLD&  left, const VLD& right);

};